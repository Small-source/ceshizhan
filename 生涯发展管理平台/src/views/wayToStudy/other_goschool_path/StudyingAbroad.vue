                <p>高考成绩在一本至二本分数线之间可推荐报读新加坡管理学院、新加坡东亚管理学院、新加坡管理发展学院、詹姆斯库克大学新加坡校区、新加坡博伟教育集团——O/A水准课程、新加坡物流管理学院、新加坡市场管理学院、新加坡英华美学院、柯廷科技大学新加坡校区。语言要求是雅思5.5分、部分课程雅思6分或6.5分；若无雅思成绩可申请语言班；高中毕业或完成高二课程、高中平均成绩70分以上。</p>
                <p>高考成绩在三本分数线左右可以报读博伟教育集团——证书课程、新加坡市场管理学——证书课程。要求语言成绩雅思5分或5.5分；应届高中毕业生、或完成高二课程的学生。</p>
