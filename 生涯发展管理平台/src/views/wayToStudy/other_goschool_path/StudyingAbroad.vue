            <h6>高中生毕业生应依据特长选专业</h6>
            <p>兴趣是学习的最大动力，对于初出茅庐的高中生来说，因首先结合自己的专业特长和兴趣爱好，选择较易发挥和把握的专业，这样可以学得比较轻松，尽快适应陌生的留学环境，使得心理素质逐渐增强，也可增加父母的自信心。</p>
            <p>选专业要提前为就业做准备，要根据自己的职业发展目标和就业市场的走向，选择有发展潜力的专业。</p>
            <p>目前，中国学生大多先选院校后选专业，这种做法不科学。在国外，综合实力不强的学校并不意味着没有好的专业，而综合实力很强的学校也并非所有的专业都名列前茅。因此，先选定专业，然后才考虑在相关专业教育领域有优势的院校，这样才能提高“命中率”。</p>
            <p>从专业的角度来看，不是所有国家的专业学科都是齐全的，也不是所有国家的综合性大学都设置你喜欢的课程。而且不同国家在某些专业方面具有不同优势也是值得考虑的因素。</p>
