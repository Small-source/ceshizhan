                <p>成绩在专科线附近的学生有三种申请留学的途径：途径1：先读加拿大预科学院。这些预科学院一般都不要求提供托福成绩，大学预科需要8个月至12个月时间。被加拿大大学录取不需要考大学入学考试，但要求托福或雅思成绩，录取准则以大学预科科目的总平均分数为主。有不少学院还提供优秀大学“双录取”项目而免托福或雅思成绩。途径2：选择大学学分课程，1年后转换大学学分到大学完成学士学位。这是非常理想的升读加拿大一流大学的途径。途径3：选择入读公立社区学院，修读大学一年级、二年级课程，再转到承认学分的加拿大大学。</p>
