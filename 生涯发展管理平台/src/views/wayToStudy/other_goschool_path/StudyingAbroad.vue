            <h6>看高考成绩选留学地</h6>
            <div class="main">
                <p class="main_title">美国</p>
                <p>高考成绩不作为录取标准</p>
                <p>申请美国的院校，有无高考成绩，并不会直接作为是否录取的标准。一般来讲，申请美国院校需要托福和SAT 成绩，但是每间院校所要求的条件不同。</p>
                <p>有些学生没有托福成绩，或托福成绩不够90分，或者没有SAT 成绩，仍然可以申请到不错的美国院校。考生如果能被录取到国内的本科院校，可以一边读本科一边申请美国院校，如果所读的本科专业与所申请院校的专业相关，还可以实现转学分。</p>
                <p>对于高考失利或者是考不上自己理想学校而不读国内大学的学生，如果托福成绩在90分以上，加上高中阶段成绩和SAT成绩，可以直接申请美国的院校。高中成绩平均80分以上，托福100分以上，不管有没有SAT成绩都可以申请到全美前100名的大学。SAT1与SAT2的成绩很好的学生，可以有很大机会申请到奖学金。</p>
                <p>就算是考上中国的名校，因为对中国大学的现状非常失望而选择去美国读书。由于之前没有参加托福或者雅思考试，而又希望尽快成行，可以借助有条件录取（语言+本科双录取）前往美国读大学，一、二年级再转读美国名校，或者本科毕业升读更好的研究生院。学生最快可以在2010年9、10月份前往美国。</p>
